package org.koteyka.columns.param;

public class Cords {
    public final double x;
    public final double y;
    public final double z;
    public final float p;

    public Cords(double x, double y, double z, float p) {
        this.x = x;
        this.y = y;
        this.z = z;
        this.p = p;
    }
}
